def normalize_title(title: str) -> str:
    return title.strip().lower()


def get_description(job_title: str) -> str:
    title = normalize_title(job_title)

    # 1. Exact match
    if title in career_explanations:
        return career_explanations[title]

    # 2. Partial match (fallback)
    for key in career_explanations:
        if title in key or key in title:
            return career_explanations[key]

    return "No description available."